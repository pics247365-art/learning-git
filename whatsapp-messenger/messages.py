def get_random_message() -> str:
    return random.choice(COOL_MESSAGES)


def get_time_based_message() -> str:
    hour = datetime.now().hour
    if 5 <= hour < 12:
        return random.choice(MORNING_MESSAGES)
    elif 12 <= hour < 17:
        return random.choice(AFTERNOON_MESSAGES)
    elif 17 <= hour < 21:
        return random.choice(EVENING_MESSAGES)
    else:
        return random.choice(NIGHT_MESSAGES)


def get_personalized_message(name: str) -> str:
    base = get_random_message()
    greetings = [f"היי {name}! ", f"שלום {name}! ", f"{name}, "]
    return random.choice(greetings) + base
